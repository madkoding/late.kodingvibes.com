const EMOJI_BY_NAME = new Map(EMOJIS.map(e => [e.name, e]))

export function getEmoji(name: string): EmojiDef | null {
  return EMOJI_BY_NAME.get(name) ?? null
}

export function listEmojis(): EmojiDef[] {
  return EMOJIS
}

/**
 * Shortcode syntax: `:name:`. Matches a known emoji name
 * (longest-first so 'thumbsup' beats 'thumbs' if it ever
 * existed). Outside `:...:` it is just plain text.
 */
const SHORTCODE_RE = (() => {
  const names = Array.from(EMOJI_BY_NAME.keys()).sort((a, b) => b.length - a.length)
  return new RegExp(`:(${names.map(n => n.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')).join('|')}):`, 'g')
})()

/**
 * Replace every `:name:` shortcode in `text` with the
 * matching emoji's inline SVG. If the shortcode doesn't
 * match a known emoji it is left untouched.
 */
export function renderEmojiShortcodes(text: string): string {
  return text.replace(SHORTCODE_RE, (match, name: string) => {
    const def = EMOJI_BY_NAME.get(name)
    if (!def) return match
    return def.svg
  })
}
